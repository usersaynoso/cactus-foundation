#!/usr/bin/env node
import { existsSync, readdirSync, readFileSync, writeFileSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const rootDir = join(__dirname, '..')
const modulesDir = join(rootDir, 'modules')
const outPath = join(rootDir, 'lib', 'puck', 'module-components.ts')

function getModuleNames() {
  if (!existsSync(modulesDir)) return []
  return readdirSync(modulesDir, { withFileTypes: true })
    .filter(e => e.isDirectory())
    .map(e => e.name)
    .sort()
}

const moduleNames = getModuleNames()
const imports = []
const clientEntries = []
const rscEntries = []

for (const moduleName of moduleNames) {
  const manifestPath = join(modulesDir, moduleName, 'cactus.module.json')
  if (!existsSync(manifestPath)) continue

  let manifest
  try {
    manifest = JSON.parse(readFileSync(manifestPath, 'utf8'))
  } catch {
    console.warn(`[generate-module-puck] Could not parse ${manifestPath} — skipping`)
    continue
  }

  const puckBlocks = manifest.puckBlocks
  if (!Array.isArray(puckBlocks) || puckBlocks.length === 0) continue

  for (const block of puckBlocks) {
    if (!block.type || !block.import || !block.component) {
      console.warn(`[generate-module-puck] Invalid puckBlock entry in ${moduleName} — skipping`)
      continue
    }

    const importPath = block.import.replace(/^\.\//, `@/modules/${moduleName}/`)
    const safeModule = moduleName.replace(/-/g, '_')
    const clientIdent = `_${safeModule}_${block.component}`

    if (block.rscComponent) {
      const rscIdent = `_${safeModule}_${block.rscComponent}`
      imports.push(`import { ${block.component} as ${clientIdent}, ${block.rscComponent} as ${rscIdent} } from '${importPath}'`)
      clientEntries.push(`  ${JSON.stringify(block.type)}: ${clientIdent},`)
      rscEntries.push(`  ${JSON.stringify(block.type)}: ${rscIdent},`)
    } else {
      imports.push(`import { ${block.component} as ${clientIdent} } from '${importPath}'`)
      clientEntries.push(`  ${JSON.stringify(block.type)}: ${clientIdent},`)
      rscEntries.push(`  ${JSON.stringify(block.type)}: ${clientIdent},`)
    }
  }
}

const out = []
out.push(`// AUTO-GENERATED by scripts/generate-module-puck.mjs`)
out.push(`// DO NOT EDIT BY HAND. Rewritten on every build and dev start.`)
out.push(``)
for (const imp of imports) out.push(imp)
if (imports.length > 0) out.push(``)

out.push(`// eslint-disable-next-line @typescript-eslint/no-explicit-any`)
out.push(`export const moduleComponents: Record<string, any> = {`)
for (const e of clientEntries) out.push(e)
out.push(`}`)
out.push(``)

out.push(`// eslint-disable-next-line @typescript-eslint/no-explicit-any`)
out.push(`export const moduleRscComponents: Record<string, any> = {`)
for (const e of rscEntries) out.push(e)
out.push(`}`)

writeFileSync(outPath, out.join('\n') + '\n')
console.log(
  `[generate-module-puck] module-components.ts written (${clientEntries.length} block(s): ${clientEntries.map(e => e.trim().split(':')[0].replace(/"/g, '')).join(', ') || 'none'})`
)
