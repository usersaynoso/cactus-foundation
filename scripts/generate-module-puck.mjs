#!/usr/bin/env node
import { existsSync, readdirSync, readFileSync, writeFileSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const rootDir = join(__dirname, '..')
const modulesDir = join(rootDir, 'modules')
// Two separate output files, not one. Puck blocks with server-only RSC renders
// (cookies() via next/headers) must never be statically reachable from
// lib/puck/config.tsx, which the client Puck editors import directly — even a
// single shared aggregator file with both editor-safe and RSC imports taints
// the whole file for every consumer, editor included.
const clientOutPath = join(rootDir, 'lib', 'puck', 'module-components.ts')
const rscOutPath = join(rootDir, 'lib', 'puck', 'module-rsc-components.ts')

function getModuleNames() {
  if (!existsSync(modulesDir)) return []
  return readdirSync(modulesDir, { withFileTypes: true })
    .filter(e => e.isDirectory())
    .map(e => e.name)
    .sort()
}

// Resolves a manifest import spec (e.g. "./components/puck/StructuredDataBlock")
// to a real file on disk, the way a bundler would: an exact file with a known
// extension, or a directory index. A module still under construction (or a bad
// manifest) can declare a block whose component file doesn't exist yet; emitting
// an import to it would fail the whole build and take every other module's blocks
// - and both Puck editors and the public frontend - down with it. So we resolve
// first and skip anything missing, loudly, rather than poison the generated file.
const IMPORT_EXTS = ['.ts', '.tsx', '.js', '.jsx', '.mjs', '.cjs']
function moduleImportExists(moduleName, importSpec) {
  const base = join(modulesDir, moduleName, importSpec.replace(/^\.\//, ''))
  for (const ext of IMPORT_EXTS) {
    if (existsSync(base + ext)) return true
    if (existsSync(join(base, `index${ext}`))) return true
  }
  return false
}

const moduleNames = getModuleNames()
const clientImports = []
const rscImports = []
const clientEntries = []
const rscEntries = []
// layoutType -> [{ type, clientIdent, rscIdent }]
const byLayoutType = new Map()

for (const moduleName of moduleNames) {
  const manifestPath = join(modulesDir, moduleName, 'cactus.module.json')
  if (!existsSync(manifestPath)) continue

  let manifest
  try {
    manifest = JSON.parse(readFileSync(manifestPath, 'utf8'))
  } catch {
    console.warn(`[generate-module-puck] Could not parse ${manifestPath} — skipping`)
    continue
  }

  const puckBlocks = manifest.puckBlocks
  if (!Array.isArray(puckBlocks) || puckBlocks.length === 0) continue

  for (const block of puckBlocks) {
    if (!block.type || !block.import || !block.component) {
      console.warn(`[generate-module-puck] Invalid puckBlock entry in ${moduleName} — skipping`)
      continue
    }

    // Skip a block whose component file (client or, if separate, RSC) isn't on
    // disk - see moduleImportExists. Better one missing block than a build that
    // won't compile at all.
    const rscImportSpec = block.rscImport ?? block.import
    if (!moduleImportExists(moduleName, block.import) ||
        (rscImportSpec !== block.import && !moduleImportExists(moduleName, rscImportSpec))) {
      console.warn(`[generate-module-puck] ${moduleName} block "${block.type}" imports a file that doesn't exist (${block.import}) — skipping`)
      continue
    }

    const importPath = block.import.replace(/^\.\//, `@/modules/${moduleName}/`)
    const safeModule = moduleName.replace(/-/g, '_')
    const clientIdent = `_${safeModule}_${block.component}`

    clientImports.push(`import { ${block.component} as ${clientIdent} } from '${importPath}'`)
    clientEntries.push(`  ${JSON.stringify(block.type)}: ${clientIdent},`)

    let rscIdent = clientIdent
    if (block.rscComponent) {
      rscIdent = `_${safeModule}_${block.rscComponent}`
      // rscImport lets the RSC half live in its own file, away from the editor-safe
      // placeholder — needed when the RSC render uses next/headers (cookies()) or
      // other server-only APIs.
      const rscImportPath = (block.rscImport ?? block.import).replace(/^\.\//, `@/modules/${moduleName}/`)
      rscImports.push(`import { ${block.rscComponent} as ${rscIdent} } from '${rscImportPath}'`)
    } else {
      rscImports.push(`import { ${block.component} as ${rscIdent} } from '${importPath}'`)
    }
    rscEntries.push(`  ${JSON.stringify(block.type)}: ${rscIdent},`)

    if (Array.isArray(block.layoutTypes)) {
      for (const layoutType of block.layoutTypes) {
        if (!byLayoutType.has(layoutType)) byLayoutType.set(layoutType, [])
        byLayoutType.get(layoutType).push({ type: block.type, clientIdent, rscIdent })
      }
    }
  }
}

const clientOut = []
clientOut.push(`// AUTO-GENERATED by scripts/generate-module-puck.mjs`)
clientOut.push(`// DO NOT EDIT BY HAND. Rewritten on every build and dev start.`)
clientOut.push(`// Client-safe only — see module-rsc-components.ts for the RSC render half.`)
clientOut.push(``)
for (const imp of clientImports) clientOut.push(imp)
if (clientImports.length > 0) clientOut.push(``)

clientOut.push(`// eslint-disable-next-line @typescript-eslint/no-explicit-any`)
clientOut.push(`export const moduleComponents: Record<string, any> = {`)
for (const e of clientEntries) clientOut.push(e)
clientOut.push(`}`)
clientOut.push(``)

clientOut.push(`// eslint-disable-next-line @typescript-eslint/no-explicit-any`)
clientOut.push(`export const moduleComponentsByLayoutType: Record<string, Record<string, any>> = {`)
for (const [layoutType, blocks] of byLayoutType) {
  clientOut.push(`  ${JSON.stringify(layoutType)}: {`)
  for (const b of blocks) clientOut.push(`    ${JSON.stringify(b.type)}: ${b.clientIdent},`)
  clientOut.push(`  },`)
}
clientOut.push(`}`)

writeFileSync(clientOutPath, clientOut.join('\n') + '\n')

const rscOut = []
rscOut.push(`// AUTO-GENERATED by scripts/generate-module-puck.mjs`)
rscOut.push(`// DO NOT EDIT BY HAND. Rewritten on every build and dev start.`)
rscOut.push(`// Server-only — imported only from lib/puck/config.rsc.tsx and other`)
rscOut.push(`// Server Component render paths. Never import this from a client component.`)
rscOut.push(``)
for (const imp of rscImports) rscOut.push(imp)
if (rscImports.length > 0) rscOut.push(``)

rscOut.push(`// eslint-disable-next-line @typescript-eslint/no-explicit-any`)
rscOut.push(`export const moduleRscComponents: Record<string, any> = {`)
for (const e of rscEntries) rscOut.push(e)
rscOut.push(`}`)
rscOut.push(``)

rscOut.push(`// eslint-disable-next-line @typescript-eslint/no-explicit-any`)
rscOut.push(`export const moduleRscComponentsByLayoutType: Record<string, Record<string, any>> = {`)
for (const [layoutType, blocks] of byLayoutType) {
  rscOut.push(`  ${JSON.stringify(layoutType)}: {`)
  for (const b of blocks) rscOut.push(`    ${JSON.stringify(b.type)}: ${b.rscIdent},`)
  rscOut.push(`  },`)
}
rscOut.push(`}`)

writeFileSync(rscOutPath, rscOut.join('\n') + '\n')

console.log(
  `[generate-module-puck] module-components.ts + module-rsc-components.ts written (${clientEntries.length} block(s): ${clientEntries.map(e => e.trim().split(':')[0].replace(/"/g, '')).join(', ') || 'none'})`
)
