#!/usr/bin/env node
import { existsSync, readdirSync, readFileSync, writeFileSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'
import { getModuleNames as registeredModuleNames } from './lib/module-names.mjs'

const __dirname = dirname(fileURLToPath(import.meta.url))
const rootDir = join(__dirname, '..')
const modulesDir = join(rootDir, 'modules')
const outPath = join(rootDir, 'lib', 'modules', 'sms-providers.ts')

// Registry-filtered: see scripts/lib/module-names.mjs for why a bare directory
// listing is not good enough here.
function getModuleNames() {
  return registeredModuleNames(rootDir)
}

const moduleNames = getModuleNames()
const imports = []
const entries = []
// ident -> importPath already emitted. A module can register the same export under
// two provider ids, which yields the same (ident, importPath) twice; emit the import
// once. A collision - the same ident mapping to a DIFFERENT path - is a real name
// clash and must throw rather than emit a duplicate identifier.
const emittedIdents = new Map()
function pushImport(ident, importPath, line) {
  const existing = emittedIdents.get(ident)
  if (existing === undefined) {
    emittedIdents.set(ident, importPath)
    imports.push(line)
  } else if (existing !== importPath) {
    throw new Error(`[generate-module-sms-providers] identifier ${ident} maps to two different imports: '${existing}' and '${importPath}'`)
  }
}

for (const moduleName of moduleNames) {
  const manifestPath = join(modulesDir, moduleName, 'cactus.module.json')
  if (!existsSync(manifestPath)) continue

  let manifest
  try {
    manifest = JSON.parse(readFileSync(manifestPath, 'utf8'))
  } catch {
    console.warn(`[generate-module-sms-providers] Could not parse ${manifestPath} — skipping`)
    continue
  }

  const smsProviders = manifest.smsProviders
  if (!Array.isArray(smsProviders) || smsProviders.length === 0) continue

  for (const entry of smsProviders) {
    if (!entry.id || !entry.label || !entry.import || !entry.export) {
      console.warn(`[generate-module-sms-providers] Invalid smsProviders entry in ${moduleName} — skipping`)
      continue
    }

    const importPath = entry.import.replace(/^\.\//, `@/modules/${moduleName}/`)
    const safeModule = moduleName.replace(/-/g, '_')
    const ident = `_${safeModule}_${entry.export}`

    pushImport(ident, importPath, `import { ${entry.export} as ${ident} } from '${importPath}'`)
    entries.push(`  { module: ${JSON.stringify(moduleName)}, id: ${JSON.stringify(entry.id)}, label: ${JSON.stringify(entry.label)}, provider: ${ident} },`)
  }
}

const out = []
out.push(`// AUTO-GENERATED by scripts/generate-module-sms-providers.mjs`)
out.push(`// DO NOT EDIT BY HAND. Rewritten on every build and dev start.`)
out.push(``)
for (const imp of imports) out.push(imp)
if (imports.length > 0) out.push(``)

out.push(`// eslint-disable-next-line @typescript-eslint/no-explicit-any`)
out.push(`export const moduleSmsProviders: Array<{ module: string; id: string; label: string; provider: any }> = [`)
for (const e of entries) out.push(e)
out.push(`]`)

writeFileSync(outPath, out.join('\n') + '\n')
console.log(
  `[generate-module-sms-providers] sms-providers.ts written (${entries.length} provider(s): ${entries.map(e => e.match(/id: "([^"]+)"/)?.[1]).filter(Boolean).join(', ') || 'none'})`
)
