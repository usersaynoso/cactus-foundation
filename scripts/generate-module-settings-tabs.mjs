#!/usr/bin/env node
import { existsSync, readdirSync, readFileSync, writeFileSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'
import { getModuleNames as registeredModuleNames } from './lib/module-names.mjs'

const __dirname = dirname(fileURLToPath(import.meta.url))
const rootDir = join(__dirname, '..')
const modulesDir = join(rootDir, 'modules')
const outPath = join(rootDir, 'lib', 'modules', 'settings-tabs.ts')

// Registry-filtered: see scripts/lib/module-names.mjs for why a bare directory
// listing is not good enough here.
function getModuleNames() {
  return registeredModuleNames(rootDir)
}

// Resolves a manifest import spec to a real file on disk, the way a bundler
// would. A module still under construction can declare a settings tab whose
// component file doesn't exist yet; emitting an import to it would fail the whole
// build, so we resolve first and skip anything missing rather than poison the
// generated file. Mirrors the guard in generate-module-puck.mjs.
const IMPORT_EXTS = ['.ts', '.tsx', '.js', '.jsx', '.mjs', '.cjs']
function moduleImportExists(moduleName, importSpec) {
  const base = join(modulesDir, moduleName, importSpec.replace(/^\.\//, ''))
  for (const ext of IMPORT_EXTS) {
    if (existsSync(base + ext)) return true
    if (existsSync(join(base, `index${ext}`))) return true
  }
  return false
}

const moduleNames = getModuleNames()
const imports = []
const entries = []
// ident -> importPath already emitted. A module can contribute the same component
// to two settings tabs, which yields the same (ident, importPath) twice; emit the
// import once. A collision - the same ident mapping to a DIFFERENT path - is a real
// name clash and must throw rather than emit a duplicate identifier.
const emittedIdents = new Map()
function pushImport(ident, importPath, line) {
  const existing = emittedIdents.get(ident)
  if (existing === undefined) {
    emittedIdents.set(ident, importPath)
    imports.push(line)
  } else if (existing !== importPath) {
    throw new Error(`[generate-module-settings-tabs] identifier ${ident} maps to two different imports: '${existing}' and '${importPath}'`)
  }
}

for (const moduleName of moduleNames) {
  const manifestPath = join(modulesDir, moduleName, 'cactus.module.json')
  if (!existsSync(manifestPath)) continue

  let manifest
  try {
    manifest = JSON.parse(readFileSync(manifestPath, 'utf8'))
  } catch {
    console.warn(`[generate-module-settings-tabs] Could not parse ${manifestPath} — skipping`)
    continue
  }

  const settingsTabs = manifest.settingsTabs
  if (!Array.isArray(settingsTabs) || settingsTabs.length === 0) continue

  for (const tab of settingsTabs) {
    if (!tab.id || !tab.import || !tab.component) {
      console.warn(`[generate-module-settings-tabs] Invalid settingsTabs entry in ${moduleName} — skipping`)
      continue
    }

    // Skip a tab whose component file isn't on disk - see moduleImportExists.
    if (!moduleImportExists(moduleName, tab.import)) {
      console.warn(`[generate-module-settings-tabs] ${moduleName} tab "${tab.id}" imports a file that doesn't exist (${tab.import}) — skipping`)
      continue
    }

    const importPath = tab.import.replace(/^\.\//, `@/modules/${moduleName}/`)
    const safeModule = moduleName.replace(/-/g, '_')
    const ident = `_${safeModule}_${tab.component}`

    pushImport(ident, importPath, `import { ${tab.component} as ${ident} } from '${importPath}'`)
    entries.push(`  ${JSON.stringify(tab.id)}: ${ident},`)
  }
}

const out = []
out.push(`// AUTO-GENERATED by scripts/generate-module-settings-tabs.mjs`)
out.push(`// DO NOT EDIT BY HAND. Rewritten on every build and dev start.`)
out.push(``)
for (const imp of imports) out.push(imp)
if (imports.length > 0) out.push(``)

out.push(`// eslint-disable-next-line @typescript-eslint/no-explicit-any`)
out.push(`export const moduleSettingsTabComponents: Record<string, any> = {`)
for (const e of entries) out.push(e)
out.push(`}`)

writeFileSync(outPath, out.join('\n') + '\n')
console.log(
  `[generate-module-settings-tabs] settings-tabs.ts written (${entries.length} tab(s): ${entries.map(e => e.trim().split(':')[0].replace(/"/g, '')).join(', ') || 'none'})`
)
