// Transpiles the Cloudflare media Worker (workers/media-worker/index.ts) to a
// plain ES module and embeds it as a string constant the app can read at runtime.
//
// Why: the "Deploy Worker automatically" flow (admin > config > Media) uploads
// this Worker to the user's Cloudflare account via the Cloudflare API. The upload
// needs the Worker as runnable JavaScript, but `typescript` is a devDependency and
// is pruned from the production runtime, so we cannot transpile on demand there.
// Generating a bundled string module at build time makes the Worker source part of
// the serverless bundle with no runtime transpile and no filesystem tracing.
//
// The Worker imports nothing, so a single-file type-strip is all that is required.
// Output is git-ignored (via .git/info/exclude) and regenerated on every dev/build,
// exactly like the other generated files in lib/.

import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'
import ts from 'typescript'

const here = dirname(fileURLToPath(import.meta.url))
const root = join(here, '..')
const SRC = join(root, 'workers', 'media-worker', 'index.ts')
const OUT = join(root, 'lib', 'media', 'worker-source.generated.ts')

const source = readFileSync(SRC, 'utf8')

const { outputText } = ts.transpileModule(source, {
  compilerOptions: {
    target: ts.ScriptTarget.ES2022,
    module: ts.ModuleKind.ESNext,
    removeComments: false,
  },
  fileName: 'index.ts',
})

const banner =
  '// AUTO-GENERATED by scripts/generate-media-worker.mjs - do not edit.\n' +
  '// Source: workers/media-worker/index.ts\n'

const body =
  banner +
  'export const MEDIA_WORKER_SOURCE: string = ' +
  JSON.stringify(outputText) +
  '\n'

writeFileSync(OUT, body, 'utf8')
console.log(`[generate-media-worker] wrote ${OUT} (${outputText.length} bytes of JS)`)
